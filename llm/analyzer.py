"""
LLM Analyzer – uses free Google Gemini API for market analysis.
"""

import logging
import os
from typing import Optional

logger = logging.getLogger(__name__)


def _ask_gemini(prompt: str, max_tokens: int = 512) -> Optional[str]:
    """Send a prompt to Gemini and return the text response."""
    api_key = os.getenv("GEMINI_API_KEY", "")
    if not api_key:
        return None
    try:
        from google import genai
        client   = genai.Client(api_key=api_key)
        response = client.models.generate_content(
            model="gemini-2.0-flash",
            contents=prompt,
        )
        return response.text.strip()
    except ImportError:
        try:
            import google.generativeai as genai_legacy
            genai_legacy.configure(api_key=api_key)
            model    = genai_legacy.GenerativeModel("gemini-1.5-flash")
            response = model.generate_content(prompt)
            return response.text.strip()
        except Exception as exc:
            logger.warning(f"Gemini legacy fallback failed: {exc}")
            return None
    except Exception as exc:
        logger.warning(f"Gemini request failed: {exc}")
        return None


